    private void calculate(){
        JOptionPane.showMessageDialog(this, " Calculate");
        this.percentage=(o_sum*100)/t_sum;
        if(percentage >=90){
            this.grade="A+";
        }
        else if(percentage >= 80){
            this.grade="A";
        }
        else if(percentage >= 70){
            this.grade="B";
        }
        else if(percentage >= 60){
            this.grade="C";
        }
        else if(percentage >= 33){
            this.grade="D";
        }
        else{
            this.grade="F";
        }
        return;
    }
