    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        int score[]= new int[8];
        score[0]=Integer.parseInt(jTextField_id.getText());
        score[1]=Integer.parseInt(jTextField_E_Omarks.getText());
        score[2]=Integer.parseInt(jTextField_S_Omarks.getText());
        score[3]=Integer.parseInt(jTextField_M_Omarks.getText());
        score[4]=Integer.parseInt(jTextField_SI_OMarks.getText());
        score[5]=Integer.parseInt(jTextField_U_Omarks.getText());
        score[6]=Integer.parseInt(jTextField_P_Omarks.getText());
        score[7]=Integer.parseInt(jTextField_I_Omarks.getText());
        String[] subj=new String[]{"English","Science","Math","Sindhi","Urdu","P.Studies","Isl/Ikh"};
        boolean check=false;
        for(int i=0;i<7;i++){
            try {
                mystmt=db.con.prepareCall("{call result_insertion(?,?,?,?,?,?)}");
                mystmt.setInt(1,score[0]);
                mystmt.setString(2,c_id);
                mystmt.setString(3,t_year);
                mystmt.setString(4,session);
                mystmt.setString(5,subj[i]);
                mystmt.setInt(6,score[i+1]);
                boolean hasresult=mystmt.execute();
                if(!hasresult){
                    check=true;
                    jTextField_id.setText("");
                    jTextField_E_Omarks.setText("");
                    jTextField_S_Omarks.setText("");
                    jTextField_M_Omarks.setText("");
                    jTextField_SI_OMarks.setText("");
                    jTextField_U_Omarks.setText("");
                    jTextField_P_Omarks.setText("");
                    jTextField_I_Omarks.setText("");
        
                }
            } catch (SQLException ex) {
                Logger.getLogger(Result_Insertion.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if(check && session.equals("Final")){
            
            try {
                mystmt=db.con.prepareCall("{call final_result(?,?,?,?)}");
                mystmt.setInt(1,score[0]);
                mystmt.setString(2,c_id);
                mystmt.setString(3,session);
                mystmt.setString(4,t_year);
                boolean hr=mystmt.execute();
                if(!hr){
                    System.out.println("\n  DONE \n");
                }
            } catch (SQLException ex) {
                Logger.getLogger(Result_Insertion.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButtonAddActionPerformed

